    private void addtocartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addtocartActionPerformed
        // TODO add your handling code here:
        String id = idfield.getText();
        String name = namefield.getText().trim();
        String type = typefield.getText().trim();
        String price = pricefield.getText().trim();
        String quantity = quantityfield.getText().trim();
        
        if ("".equals(name)|| "".equals(type) || "".equals(price)){
            notify.setText("Kindly select the product to add to cart from the list below");
        }
        else if("".equals(quantity) || Integer.parseInt(quantity) == 0){
            notify.setText("Enter the quantity(must be > 0");
        }
        else {
            try {
                Store stor = new Store();
                Connection con = stor.createConnection();
                Statement stm = null;
                stm = con.createStatement();
                String sqly = "SELECT * from cart WHERE name='"+name+"'";
                ResultSet r=stm.executeQuery(sqly);
                if(r.next()){
                    int new_quantity = Integer.parseInt(quantity) + r.getInt("quantity");
                    String sql3 = "UPDATE cart set quantity = '"+new_quantity+"' WHERE name= '"+name+"'";
                    stm.executeUpdate(sql3);
                    notify.setText("Product added to cart");
                    JOptionPane.showMessageDialog(jPanel1,
                    "Added to cart",
                    "A plain message",
                    JOptionPane.PLAIN_MESSAGE);
                }else{
                    
                String sql3 = "INSERT INTO cart (name, type, price, quantity)"
                        + " VALUES('"+name+"','"+type+"',"
                        + " '"+Integer.parseInt(price)+"',"
                        + " '"+Integer.parseInt(quantity)+"')";
                stm.executeUpdate(sql3);
                notify.setText("Product added to cart");
                JOptionPane.showMessageDialog(jPanel1,
                    "Added to cart",
                    "A plain message",
                    JOptionPane.PLAIN_MESSAGE);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Items.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_addtocartActionPerformed

