    public void getItems(){
         try {
            initComponents();
            DefaultTableModel model = new DefaultTableModel(new String[]{"Id","Name", "Type", "price"}, 0);

            Store stor = new Store();
            Connection con = stor.createConnection();
            Statement stm = null;
            stm = con.createStatement();
            String sql = "SELECT * FROM products";
            ResultSet rs = stm.executeQuery(sql);
            
            while(rs.next())
            {
                int c = rs.getInt("id");
                String d = rs.getString("name");
                String e = rs.getString("type");
                String f = rs.getString("price");
                model.addRow(new Object[]{c,d, e, f});
            }
            productstable.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(Items.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
